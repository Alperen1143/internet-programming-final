              <p>Arjantinli yıldız futbolcu Lionel Messi, Dünya Kupası'nda en fazla forma giyen ve süre alan futbolcu olarak tarihe geçti.

Lusail Stadı'nda oynanan 2022 FIFA Dünya Kupası finalinde Fransa'ya karşı sahaya çıkan 35 yaşındaki Messi, turnuvada 26. kez forma giyerek Alman futbolcu Lothar Matthaus'u geride bıraktı.

Arjantinli yıldız, turnuvada aldığı süreyle de yeni rekorun sahibi oldu. Messi, bu alanda 2217 dakikayla rekoru elinde tutan İtalyan Paolo Maldini'yi geçti.

Messi, Dünya Kupası'nda 19 kezle en çok kaptan olarak sahaya çıkan futbolcu konumunda. Arjantinli oyuncuyu 17 maçla Meksikalı Rafa Marquez ve 16 maçla Diego Maradona izliyor.

35 yaşındaki Messi, Dünya Kupası'nda onlu, yirmili ve otuzlu yaşlarında gol atan tek oyuncu. Pele, aynı başarıyı dört ayla kaçırmıştı.

Yarı finalde Hırvatistan karşısında 34. dakikada penaltı atışını gole çeviren Messi, Gabriel Batistuta'yı geride bırakarak 11 golle Dünya Kupası'nda en fazla fileleri havalandıran Arjantinli futbolcu unvanını elde etmişti.</p>
              
              
