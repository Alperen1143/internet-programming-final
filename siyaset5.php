              <p>İçişleri Bakanlığı, İstanbul Büyükşehir Belediye Başkanlığı'na yönelik incelemede görevli müfettişin değiştirilmesine yönelik iddialara yanıt verdi. Müfettişin sağlık sorunları nedeniyle affını istediği belirtilen açıklamada, sağlık, eğitim veya ailevi durumlar gibi gerekçelere bağlı olarak yapılan görev değişikliklerinin yıl içinde diğer teftiş ve soruşturmalarda da uygulandığı hatırlatılarak, rutin uygulamanın farklı yönlere çekilmesinin asla kabul edilemeyeceği vurgulandı.</p>
               <p>İçişleri Bakanlığı Mülkiye Teftiş Kurulu Başkanlığı'ndan İstanbul Büyükşehir Belediyesi'ni (İBB) teftiş eden müfettişlerden birinin "sağlık" sorunları nedeniyle affını istediği, bazı haberlerde yer alan iddiaların mesnetsiz olduğu bildirildi.

Bakanlıktan yapılan açıklamada, "Sağlık, eğitim veya ailevi durumlar gibi gerekçelere bağlı olarak yapılan görev değişiklikleri yıl içinde diğer teftiş ve soruşturmalarda da uygulanmıştır" denildi. </p>
              
                <p> Açıklamada, "Olağan çalışma usulleri içerisinde yapılan rutin uygulamaların farklı yönlere çekilmesi ve farklı anlamlar yüklenmesinin asla kabul edilemeyeceği" vurgulandı. İstanbul Büyükşehir Belediyesi'nde yürütülen teftiş ve soruşturmalarla ilgili bakanlığın defalarca açıklama yaptığı hatırlatıldı, buna rağmen mesnetsiz iddialarla kamuoyunun yanıltılmaya çalışıldığı belirtildi.

Açıklamada şunlar kaydedildi:

"Bakanlık makamının 20 Aralık 2021 tarihli onayı kapsamında 5 mülkiye müfettişi, 1 ticaret müfettişi, 1 hazine ve maliye müfettişi ile 1 MASAK uzmanından müteşekkil 8 kişilik teftiş heyeti tarafından İstanbul Büyükşehir Belediyesinde özel teftiş yürütülmüştür. Her onay kapsamında yapıldığı gibi bu onay kapsamında da görevi layıkıyla tamamlayacak sayıda müfettiş görevlendirilmiştir.

İnceleme, teftiş ve soruşturma için görevlendirme yapılırken müfettişlerin üzerindeki iş yükü ve Kurulun iş yoğunluğu dikkate alınmaktadır. Yine işin durumuna göre ilave müfettiş görevlendirmesi yapıldığı gibi müfettişlerin mazeretlerine binaen üzerlerindeki görevler de alınabilmektedir. İstanbul Büyükşehir Belediyesi özel teftişinde 24 Aralık 2021 tarihinde görevlendirilen ve gündeme getirilen müfettişimiz, 11 Nisan 2022 tarihli dilekçesiyle 'kendisinin ve eşinin sağlık sorunları bulunması ve tedavi süreci' gerekçesiyle teftiş görevinden affını talep etmiştir.</p>


              
              
              
                
